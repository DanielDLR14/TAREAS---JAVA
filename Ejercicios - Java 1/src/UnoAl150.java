public class UnoAl150 {
    public static void main(String[] args) {
        // Declara una variable entera y asignale el valor 1
        int num = 1;
        // Utiliza un bucle while para iterar mientras num sea menor o igual a 150
        while (num <= 150) {
            // Imprime el valor de la variable num
            System.out.println(num);
            // Incrementa num en 1
            num++;
        }
    }
}
