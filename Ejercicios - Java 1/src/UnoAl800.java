public class UnoAl800 {
    public static void main(String[] args) {
        // Declara una variable entera y asignale el valor 1
        int num = 1;
        // Se utiliza un bucle while para decir mientras num sea menor o igual a 800
        while (num <= 800) {
            // Imprime el valor de la variable num
            System.out.println(num);
            // Incrementa num en 5
            num += 5;
        }
    }
}
