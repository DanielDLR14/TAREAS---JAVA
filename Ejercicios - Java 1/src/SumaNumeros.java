// Se crea nueva clase con el nombre que desees.
public class SumaNumeros {
    /*
    public static void main(String[] args) es el método principal en una aplicación Java.
    Es el punto de entrada para la ejecución de un programa Java y
    se usa para indicar dónde empezará la ejecución del programa.
    */
    public static void main(String[] args) {

        // Declara tres variables enteras para almacenar los números que quieres sumar.
        int num1 = 10;
        int num2 = 20;
        int num3 = 30;

        // Calcula la suma de los tres números y almacena el resultado en una variable.
        int suma = num1 + num2 + num3;

        // Utiliza la instrucción System.out.println para imprimir el resultado de la suma en consola.
        System.out.println("La suma de los números es: " + suma);
    }
}
