// Se asgina un nombre a la clase
public class IgualdadNumeros {
    public static void main(String[] args) {
        // Se asignan nombres a las variables donde iran los numeros
        int num1 = 10; // primer número
        int num2 = 20; // segundo número
        /*
        El programa compara los valores de las variables num1 y num2 utilizando el operador de igualdad (==).
        Si ambos valores son iguales, se imprime el mensaje
        "Son iguales", de lo contrario se imprime "No son iguales".
        */
        if (num1 == num2) {
            System.out.println("Son iguales");
        } else {
            System.out.println("No son iguales");
        }
    }
}
